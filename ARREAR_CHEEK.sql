AND MD.BILL_NUMBER=B.BILL_NUMBER
--AND MD.PAY_DATE BETWEEN '01-DEC-2016' AND '31-DEC-2016'
AND B.LOCATION_CODE='J6'
AND B.BILL_MONTH='201611'
)) GROUP BY  BILL_CYCLE_CODE,LOCATION_CODE,BILL_GROUP 







