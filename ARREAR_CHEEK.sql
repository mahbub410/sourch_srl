WHERE (NVL(ARR_ADV_ADJ_PRN,0) >0 OR NVL(ARR_ADV_ADJ_VAT,0)>0) 
AND 
 INVOICE_NUM IN (
SELECT SUBSTR(BILL_NUMBER,1,8) FROM EPAY_UTILITY_BILL
WHERE BILL_MONTH='201612'
AND LOCATION_CODE='J6'
AND (NVL(ARREAR_PRINCIPLE,0)>0 OR NVL(ARREAR_GOVT_DUTY,0)>0)
AND ACCOUNT_NUMBER IN (
SELECT B.ACCOUNT_NUMBER FROM VW_EPAY_PAYMENT_MST_DTL MD,EPAY_UTILITY_BILL B
