select TRUNC(TRANS_DATE),ITEM_CODE,
(NVL(SUM(NVL(DB_QNTY,0))-SUM(NVL(CR_QNTY,0)),0))-(0.4898) o,
NVL(SUM(NVL(DB_QNTY,0))-SUM(NVL(CR_QNTY,0)),0) OPNEING_QNTY_C3,
