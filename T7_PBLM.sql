---ROBI----

SELECT M.PAY_DATE,M.LOCATION_CODE,SUM(D.PDB_AMOUNT) PDB_AMOUNT,SUM(D.GOVT_DUTY) VAT,SUM(D.PDB_AMOUNT+D.GOVT_DUTY) TOT_AMOUNT,COUNT(*) TOT_CONSUMER 
FROM PAYMENT_MST@EPAY_ROBI M,PAYMENT_DTL@EPAY_ROBI D
WHERE M.BATCH_NO=D.BATCH_NO
AND M.LOCATION_CODE='T1'
AND TO_CHAR(M.PAY_DATE,'RRRRMM')=:P_MNTH
GROUP  BY M.PAY_DATE,M.LOCATION_CODE
ORDER BY 1 ASC

---PDB----

SELECT M.PAY_DATE,M.LOCATION_CODE,SUM(D.PDB_AMOUNT) PDB_AMOUNT,SUM(D.GOVT_DUTY) VAT,SUM(D.PDB_AMOUNT+D.GOVT_DUTY) TOT_AMOUNT,COUNT(*) TOT_CONSUMER 
FROM EPAY_PAYMENT_MST M,EPAY_PAYMENT_DTL D
WHERE M.BATCH_NO=D.BATCH_NO
AND M.LOCATION_CODE='T1'
AND TO_CHAR(M.PAY_DATE,'RRRRMM')=:P_MNTH
GROUP  BY M.PAY_DATE,M.LOCATION_CODE
ORDER BY 1 ASC