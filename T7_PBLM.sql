-------BILING

SELECT A.RECEIPT_DATE,A.LOCATION_CODE,SUM(B.RECEIPT_AMT) PDB_AMOUNT,SUM(B.VAT_AMT) VAT,SUM(B.RECEIPT_AMT+B.VAT_AMT) TOT_AMOUNT,COUNT(*) TOT_CONSUMER
FROM BC_RECEIPT_BATCH_HDR@BILLING_TANG A,BC_RECEIPT_HDR@BILLING_TANG B
WHERE A.BATCH_NUM=B.BATCH_NUM
AND TO_CHAR(A.RECEIPT_DATE,'RRRRMM')=:P_MNTH
AND A.LOCATION_CODE='T1'
AND A.BANK_CODE='97'
GROUP BY A.RECEIPT_DATE,A.LOCATION_CODE
ORDER BY 1 ASC


